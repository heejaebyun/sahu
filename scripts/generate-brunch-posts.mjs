function stripMarkdown(text) {
  return text
    .replace(/^#{1,6}\s+/gm, "")
    .replace(/\*\*([^*]+)\*\*/g, "$1")
    .replace(/\*([^*]+)\*/g, "$1")
    .replace(/^- \[ \] /gm, "☐ ")
    .replace(/^- /gm, "• ")
    .replace(/\|---\|---\|---\|/g, "")
    .replace(/\|---\|---\|/g, "")
    .replace(/\|/g, "  ")
    .replace(/^>\s+/gm, "")
    .replace(/---/g, "")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/`([^`]+)`/g, "$1")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

