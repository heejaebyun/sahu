function stripMarkdown(text) {
  return text
    .replace(/^#{1,6}\s+/gm, "")        // ## 제목 → 제목
    .replace(/\*\*([^*]+)\*\*/g, "$1")   // **볼드** → 볼드
    .replace(/\*([^*]+)\*/g, "$1")       // *이탤릭* → 이탤릭
    .replace(/^- \[ \] /gm, "☐ ")        // - [ ] → ☐
    .replace(/^- /gm, "• ")             // - 항목 → • 항목
    .replace(/^\d+\. /gm, (m) => m)      // 숫자 목록은 유지
    .replace(/\|---\|---\|---\|/g, "")   // 테이블 구분선 제거
    .replace(/\|---\|---\|/g, "")
    .replace(/\|/g, "  ")               // | → 공백
    .replace(/^>\s+/gm, "")             // > 인용 제거
    .replace(/---/g, "")                // 수평선 제거
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1") // [텍스트](링크) → 텍스트
    .replace(/`([^`]+)`/g, "$1")         // `코드` → 코드
    .replace(/\n{3,}/g, "\n\n")          // 빈 줄 정리
    .trim();
}

